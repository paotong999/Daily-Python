def horserace(tianji,qiwang):
    tianji.sort()
    qiwang.sort()
    n=len(qiwang)
    al,ar = 0,n-1
    bl,br = 0,n-1
    money= 0
    while (al <= ar):
        if(tianji[al] > qiwang[bl]):
            money += 200
            al+=1
            bl+=1
        elif(tianji[al] == qiwang[bl]):
            while(al <= ar and bl <= br):
                if(tianji[ar] > qiwang[br]):
                    money += 200
                    ar-= 1
                    br-= 1
                else:
                    if(tianji[al] < qiwang[br]):
                        money -= 200
                    al+= 1
                    br-= 1
                    break
        else:
            money-= 200
            al+= 1
            br-= 1
    return money


